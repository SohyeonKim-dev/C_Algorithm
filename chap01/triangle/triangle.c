#include <stdio.h>


void triangle(int n) {
	int i = 0;
	int j = 0;
	for (i = 1; i < n+1; i++) {
		for (j = 0; j < i; j++) {
			printf("*");
		}
		printf("\n");
	}
}

int main() {
	triangle(5);
	printf("\n");

	triangle(10);
	printf("\n");

	triangle(20);
	return 0;
}

